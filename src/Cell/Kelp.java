package Cell;

public class Kelp {
	private int kelpRow;
	private int kelpColumn;
	
	public Kelp(int row, int col){
		kelpRow = row;
		kelpColumn = col;
	}

	public int getKelpColumn() {
		return kelpColumn;
	}

	public void setKelpColumn(int kelpColumn) {
		this.kelpColumn = kelpColumn;
	}

	public int getKelpRow() {
		return kelpRow;
	}

	public void setKelpRow(int kelpRow) {
		this.kelpRow = kelpRow;
	}
	
}
